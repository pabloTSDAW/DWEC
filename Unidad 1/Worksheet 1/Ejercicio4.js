var a = 3;
var b = 10;
console.log("El valor de la primera variable es " + a + " y el valor de la segunda variable es " + b);

console.log("La suma de las dos variables es igual a: " + (a+b));

console.log("La resta de las dos variables es igual a: " + (a-b));

console.log("El producto de las dos variables es igual a: " + (a*b));

console.log("El cociente entre las dos variables es igual a: " + (a/b));

console.log("La potencia de las dos variables es igual a: " + (a**b));
